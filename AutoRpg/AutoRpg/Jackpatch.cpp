string Jackpatch::GetAsciiArt()
{
    string Art = mAsciiArt[FrameIdx];
    FrameIdx = FrameIdx >= 1 ? 0 : FrameIdx + 1;
    return Art;
}
string Jackpatch::GetAsciiArtDeath()
{
    return mAsciiArt[2];
}
